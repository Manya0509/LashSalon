using System.ComponentModel.DataAnnotations;

namespace LashBooking.Web.MVC.Models
{
    public class BookingViewModel
    // Модель формы записи /Booking
    {

        [Required(ErrorMessage = "Введите имя")]
        // Поле обязательно — нельзя записаться без имени

        [StringLength(100, MinimumLength = 2, ErrorMessage = "Имя должно быть от 2 до 100 символов")]
        // Минимум 2 символа — защита от "А"
        // Максимум 100 — защита от огромных строк в базе данных
        public string ClientName { get; set; } = "";
        // Имя клиента. = "" — чтобы не было null по умолчанию


        [Required(ErrorMessage = "Введите телефон")]
        // Телефон обязателен — нужен для связи с клиентом

        [RegularExpression(@"^[\d\+\-\(\)\s]{7,20}$",
            ErrorMessage = "Введите корректный номер телефона")]
        // Проверка формата номера:
        // ^ — начало, $ — конец
        // [\d\+\-\(\)\s] — разрешены: цифры, +, -, (, ), пробел
        // {7,20} — от 7 до 20 символов
        // ✅ Пропустит: +7(999)410-38-01, 89994103801
        // ❌ Не пропустит: "абвгд", "123"
        public string ClientPhone { get; set; } = "";


        [Range(1, int.MaxValue, ErrorMessage = "Выберите услугу")]
        // Range — число должно быть в диапазоне от 1 до максимального int (2 147 483 647)
        // По умолчанию ServiceId = 0 → не пройдёт валидацию (0 < 1)
        // Это заставляет пользователя выбрать услугу из списка
        // int.MaxValue = 2 147 483 647 — практически "без верхнего предела"
        public int ServiceId { get; set; }
        // ID выбранной услуги из базы данных (например: 1 = Наращивание, 2 = Коррекция)
        // = 0 по умолчанию — специально не проходит Range(1, ...) → валидация не даст отправить


        [Required(ErrorMessage = "Выберите время записи")]
        // Время обязательно — нельзя записаться без выбора слота
        public string SelectedTime { get; set; } = "";
        // Выбранное время записи в виде строки
        // Например: "2025-06-15 14:00" или "14:00"
    }
}
